#include "operations.h"

/**
 * add -> Adds two integers
 * @a: First integer
 * @b: Second integer
 * Return: Sum of a and b
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub -> Subtracts two integers
 * @a: First integer
 * @b: Second integer
 * Return: Difference of a and b
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul -> Multiplies two integers
 * @a: First integer
 * @b: Second integer
 * Return: Product of a and b
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div -> Divides two integers
 * @a: First integer
 * @b: Second integer
 * Return: Quotient of a and b
*/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod -> Modulus of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Remainder of a and b
*/

int mod(int a, int b)
{
	return (a % b);
}
